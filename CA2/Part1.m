set(ax,'DataAspectRatio',[1 1.125 1]);
set(ax,'PlotBoxAspectRatio',[8 5.333333333333333 1]);
set(ax,'XLim',[-8 8]);
set(ax,'YLim',[-6 6]);
set(ax,'XTickMode','auto');
set(ax,'YTickMode','auto');

% Geometry description:
pderect([-6 6 4 -4],'Air_1');
pderect([-4.4000000000000004 4.4000000000000004 1.8 0.20000000000000001],'COIL');
pderect([-5.5 5.5 1.5 -1.5],'IRON');
pderect([4.5 5.5 1 -1],'AIR_3');
pderect([-4.5 5 0.5 -0.5],'AIR_2');
pderect([5 5.5 0.10000000000000001 -0.10000000000000001],'AIR_g');
set(findobj(get(pde_fig,'Children'),'Tag','PDEEval'),'String','AIR_g + AIR_3 + AIR_2 + Air_1+ IRON + COIL')

% Boundary conditions:
pdetool('changemode',0)
pdesetbd(36,...
'dir',...
1,...
'1',...
'0')
pdesetbd(35,...
'dir',...
1,...
'1',...
'0')
pdesetbd(2,...
'dir',...
1,...
'1',...
'0')
pdesetbd(1,...
'dir',...
1,...
'1',...
'0')

% Mesh generation:
setappdata(pde_fig,'Hgrad',1.3);
setappdata(pde_fig,'refinemethod','regular');
setappdata(pde_fig,'jiggle',char('on','mean',''));
setappdata(pde_fig,'MesherVersion','preR2013a');
pdetool('initmesh')

% PDE coefficients:
pdeseteq(1,...
'1./((4 * pi * 1e-7))!1./((4 * pi * 1e-7))!1./((2000 *4 * pi * 1e-7))!1./((2000 * 4 * pi * 1e-7))!1./((2000 * 4 * pi * 1e-7))!1./((4 * pi * 1e-7))!1./((4 * pi * 1e-7))!1./((2000 * 4 * pi * 1e-7))!1./(2000* (4 * pi * 1e-7))!1./((4 * pi * 1e-7))!1./((4 * pi * 1e-7))',...
